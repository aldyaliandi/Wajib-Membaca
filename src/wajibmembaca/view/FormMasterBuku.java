    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        String isbn = txtisbn.getText();
        String judul = txtjudul.getText();
        String penulis = txtpenulis.getText();
        String publisher = txtpublisher.getText();
        String tahun_terbit = txttahun.getText();
           int jumlah_halaman = Integer.parseInt(txtjumlah.getText());
                   
        if(btnsimpan.getText().equals("Simpan")){
            if(controllermaster_buku.insertmaster_buku(isbn, judul, penulis, publisher, tahun_terbit, jumlah_halaman)){
                 lblout.setText("Insert Berhasil");
            }else{
                lblout.setText("Insert Gagal");
            }
        }else{
             if(controllermaster_buku.updatemaster_buku(isbn, judul, penulis, publisher, tahun_terbit, jumlah_halaman)){
                lblout.setText("Update Berhasil");
            }else{
                lblout.setText("Update Gagal");
            }
        }
        setClear();
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void tblbukuMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblbukuMousePressed
        if(evt.getClickCount() == 2){
            int idxrow = tblbuku.getSelectedRow();
            txtisbn.setText(tblbuku.getModel().getValueAt(idxrow, 0).toString());
            txtjudul.setText(tblbuku.getModel().getValueAt(idxrow, 1).toString());
            txtpenulis.setText(tblbuku.getModel().getValueAt(idxrow, 2).toString());
            txtpublisher.setText(tblbuku.getModel().getValueAt(idxrow, 3).toString());
            txttahun.setText(tblbuku.getModel().getValueAt(idxrow, 4).toString());
            txtjumlah.setText(tblbuku.getModel().getValueAt(idxrow, 5).toString());
            btnsimpan.setText("Update");
        }
         if(evt.isPopupTrigger()){
        popupbuku.show(tblbuku, evt.getX(), evt.getY());
         }
    }//GEN-LAST:event_tblbukuMousePressed

