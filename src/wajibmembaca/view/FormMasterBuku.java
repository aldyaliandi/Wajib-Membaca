private void setTablemaster_buku(List<master_buku> listmaster_buku) {
        if (listmaster_buku==null){
            JOptionPane.showMessageDialog(this, "Data "
                    + "master_buku gagal diambil dari database","DATABASE FAILED", JOptionPane.ERROR_MESSAGE);
        }else{
            DefaultTableModel tableModel = new DefaultTableModel();
            tableModel.setColumnIdentifiers(
                    new String[] {
                        "isbn",
                        "judul",
                        "penulis",
                        "publisher",
                        "tahun terbit",
                        "jumlah halaman"
                    }
            );
            
            for (master_buku b : listmaster_buku) {
                Object[] o = new Object[6];
                o[0] = b.getIsbn();
                o[1] = b.getJudul();
                o[2] = b.getPenulis();
                o[3] = b.getPublisher();
                o[4] = b.getTahun_terbit();
                o[5] = b.getJumlah_halaman();
                tableModel.addRow(o);
            }
            tblbuku.setAutoResizeMode( javax.swing.JTable.AUTO_RESIZE_ALL_COLUMNS);
            tblbuku.setModel(tableModel);
            }
        }
